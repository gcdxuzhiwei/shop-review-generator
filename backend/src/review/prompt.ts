/**
 * 构建最终的系统提示词
 * 将基础设定与参考风格合并为完整提示词
 */
export function buildSystemPrompt(): string {
  let prompt = BASE_PROMPT;

  if (REFERENCE_STYLES.length > 0) {
    prompt += "【参考风格】\n";

    REFERENCE_STYLES.forEach((style, index) => {
      prompt += `--- 参考样例 ${index + 1}：${style.label} ---\n`;
      prompt += style.text;
    });
  }

  return prompt;
}
